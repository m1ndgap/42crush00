#include <unistd.h>

void	rush(int, int);

int main()
{
	rush(5, 5);
	write(1, "\n", 1);
	rush(1,1);
	write(1, "\n", 1);
	rush(-1,1);
	write(1, "\n", 1);
	rush(2,5);
	write(1, "\n", 1);
	rush(1,5);
	write(1, "\n", 1);
	rush(5,1);
	write(1, "\n", 1);
	rush(5,2);
	write(1, "\n", 1);
	return (0);
}